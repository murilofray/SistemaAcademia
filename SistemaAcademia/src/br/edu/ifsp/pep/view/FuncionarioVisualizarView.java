    protected void desabilitarCampos() {
        tfCpf.setEnabled(false);
        tfNome.setEnabled(false);
        tfTelefone.setEnabled(false);
        dData.setEnabled(false);
        cbSexo.setEnabled(false);
        cbCargo.setEnabled(false);
        tfLogin.setEnabled(false);
    }
    
    protected void carregarDados(Funcionario funcionarioParametro) {
        this.funcionario = funcionarioParametro;
        tfNome.setText(funcionario.getNome());
        tfCpf.setText(funcionario.getCpf());
        tfTelefone.setText(funcionario.getTelefone());
        dData.setDate(funcionario.getDataNascimento());
        tfLogin.setText(funcionario.getLogin());
        
        if (funcionario.getSexo().equals("Masculino")) {
            cbSexo.setSelectedIndex(1);
        } else {
            cbSexo.setSelectedIndex(2);
        }
        if (funcionario.getCargo().equals("Funcion√°rio")) {
            cbCargo.setSelectedIndex(1);
        } else {
            cbCargo.setSelectedIndex(2);
        }
    }

